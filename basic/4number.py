import random

#example of integer number
a = 10 
print(f"value: {a},  a is of type:", type(a))

#example of float number
b = 10.5
print(f"value: {b}, b is of type:", type(b))

#exaple of complex number
c = 2 +3j
print(f"value: {c}, c is of type:", type(c))

#example of random number
random_number = random.randrange(1, 50)
print(f"value: {random_number}, random_number is of type:", type(random_number))